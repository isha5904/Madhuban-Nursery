// Helper functions
export const getPlantById = (id) => plants.find(p => p.id === parseInt(id));

export const getPlantsByCategory = (categoryId) => plants.filter(p => p.category === categoryId);

export const getFeaturedPlants = () => plants.filter(p => p.featured);

export const formatPrice = (price) => `₹${price.toLocaleString('en-IN')}`;

export const getWhatsAppLink = (plantName) => {
  const phone = '919876543210'; // Replace with actual nursery WhatsApp number
  const message = encodeURIComponent(
    `Hi! I'm interested in the ${plantName} plant from GreenVerse Nursery. Could you share more details about availability and pricing?`
  );
  return `https://wa.me/${phone}?text=${message}`;
};

export const getStockBadgeClass = (stock) => {
  switch (stock) {
    case 'In Stock': return 'badge--instock';
    case 'Limited Stock': return 'badge--limited';
    case 'Out of Stock': return 'badge--outofstock';
    default: return '';
  }
};

export const getCareLevelBadgeClass = (level) => {
  switch (level) {
    case 'Easy': return 'badge--easy';
    case 'Medium': return 'badge--medium';
    case 'High': return 'badge--high';
    default: return '';
  }
};

export const getPriceRange = () => {
  const prices = plants.map(p => p.price);
  return { min: Math.min(...prices), max: Math.max(...prices) };
};